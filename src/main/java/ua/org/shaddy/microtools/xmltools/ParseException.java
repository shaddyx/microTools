package ua.org.shaddy.microtools.xmltools;

public class ParseException extends RuntimeException {

	/**
	 * 
	 */
	private static final long serialVersionUID = 7286132104433781503L;

	public ParseException() {
	}

	public ParseException(String arg0) {
		super(arg0);
	}

	public ParseException(Throwable arg0) {
		super(arg0);
	}

	public ParseException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}
}
